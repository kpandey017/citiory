function calculateOverallScore(scores: Omit<City["scores"], "overall">): number {
  const values = [
    scores.safety,
    scores.pollution,
    scores.internet,
    scores.qualityOfLife,
    scores.job,
    scores.healthcare,
    scores.climate,
  ]
  const average = values.reduce((sum, value) => sum + value, 0) / values.length
  return Number(average.toFixed(1))
}

